package com.bluecubs.xinco.workflow;

public class Resource {
    private int id;

    private String name;

    public Resource() {
    }

    public int getId() {
        return id;
    }

    public void setId(int val) {
        this.id = val;
    }

    public String getName() {
        return name;
    }

    public void setName(String val) {
        this.name = val;
    }
}
